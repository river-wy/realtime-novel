# ============ 默认笔风 ============

DEFAULT_PACK_ID = "yanhuo_shiyi"  # 烟火气最通用，作为 fallback


# ============ 查询方法 ============

def list_style_packs() -> List[Dict[str, Any]]:
    """返回所有笔风的摘要列表（id / name / tagline）

    供 agent 判断选哪个笔风时使用。
    """
    return [
        {"id": p["id"], "name": p["name"], "tagline": p["tagline"]}
        for p in STYLE_PACKS.values()
    ]


def get_style_pack(pack_id: str) -> Optional[Dict[str, Any]]:
    """返回完整 StylePack，不存在返回 None"""
    return STYLE_PACKS.get(pack_id)


def get_default_pack_id() -> str:
    """返回默认笔风 id（fallback 用）"""
    return DEFAULT_PACK_ID


def get_style_pack_or_default(pack_id: Optional[str]) -> Dict[str, Any]:
    """返回指定笔风，不存在或 None 时返回默认笔风"""
    if pack_id and pack_id in STYLE_PACKS:
        return STYLE_PACKS[pack_id]
    return STYLE_PACKS[DEFAULT_PACK_ID]

